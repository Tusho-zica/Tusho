# Faça um programa que pede para o usuário digitar 5 números e, ao final, imprime uma lista 
# com os 5 números digitados pelo usuário (sem converter os número para int ou float).

# Exemplo: Se o usuário digitar 1, 5, 2, 3, 6 o programa deve imprimir a lista ['1','5','2','3','6']

num0 = input("Digite o primeiro número da lista: ")
