# 3. Faça uma função para cada operação matemática básica (soma,
# subtração, multiplicação e divisão). As funções devem receber dois
# números e retornar o resultado da operação.


def soma(x, y):
    return x + y

print(soma(10, 20))

def subtracao(x, y):
    return x - y

print(subtracao(10,20))

def divisao(x, y):
    return x / y

print(divisao(10,20))

def multiplicacao(x, y):
    return x * y

print(multiplicacao(10,20))