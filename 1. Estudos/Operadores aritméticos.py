# declarando as variaveis

x = input("indique o valor de x:")
y = input("indique o valor de y:")

tempfloat1 = float(x)
tempfloat2 = float(y)

# soma
print("a soma de x e y é: \n", tempfloat1 + tempfloat2)

#subtração
print("a subtração do valor de x e y é: \n", tempfloat1 - tempfloat2)

#multiplicação
print("a multiplicação do valor de x por y é: \n", tempfloat1 * tempfloat2)

#divisão
print("a divisão do valor de x por y é: \n", tempfloat1 / tempfloat2)

#mod
print("o resto da divisão de x por y é: \n", tempfloat1 % tempfloat2)

#exponenciação
print("x elevado a y é: \n", tempfloat1 ** tempfloat2)

#inteiro da divisão
print("a divisão inteira de x por y é: \n", tempfloat1 // tempfloat2)

